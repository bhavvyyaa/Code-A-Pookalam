import turtle

b=turtle.Turtle()
b.speed(100)
for i in range(12):
    b.pu()
    b.goto(0,0)
    b.pd()
    b.pensize(5)
    b.color("#e66707")
    b.begin_fill()
    b.circle(300, 60) 
    b.left(120)
    b.pensize(5)
    b.circle(300, 60)
    b.left(120)
    b.right(30)
    b.end_fill()
b.up()
b.goto(0,0)
b.hideturtle()


